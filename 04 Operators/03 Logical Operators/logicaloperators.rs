// Logical Operators
// Logical operators operates on true or false values

// examples && , || , ! etc

// && means AND
// || means OR
// ! means NOT

fn main(){
    let a = true;
    let b = false;
    println!("Logical Operators");
    println!("AND {}" , a && b);
    println!("OR {}" , a || b);
    println!("NOT {}" , ! a);
}