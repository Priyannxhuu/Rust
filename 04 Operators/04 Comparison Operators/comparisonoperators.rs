// Comparison Operators
// Comparison Operators are used for comparing the values of two operands.

// > means Greater than
// < means Lesses than
// >= means Greater than and equals to
// <= means Lesser than and equals to
// == means Equals to
// != means Not equals to

fn main(){
    let a = 2;
    let b = 3;
    println!("Comparison Operators");
    println!("a > b {}" , a > b);
    println!("a < b {}" , a < b);
    println!("a >= b {}" , a >= b);
    println!("a <= b {}" , a <= b);
    println!("a == b {}" , a == b);
    println!("a != b {}" , a != b);
}