// Arithmatic Operators

// Arithmetic operators are used to perform arithmetic operations.

// + , - , * , / , % these are some arithmatic operators

fn main(){

    let a = 4;
    let b = 3;

    println!("Calculator");
    println!("Addition : {}" , a + b);
    println!("Subtraction : {}" , a - b);
    println!("Multiplication : {}" , a * b);
    println!("Divide : {}" , a / b);
    println!("Modulus : {}" , a % b);
}